const mongoose = require("mongoose");

const KitSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  categoryId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Category",
    required: true,
  },
});

const KitModel = mongoose.model("Kit", KitSchema);
module.exports = KitModel;